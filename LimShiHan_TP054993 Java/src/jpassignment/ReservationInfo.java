    private void btnUpdateTxTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateTxTActionPerformed
        String filePath = "ReservationInfo.txt";
        File file = new File(filePath);
       
        try {
            FileWriter fw = new FileWriter(file);
            BufferedWriter bw = new BufferedWriter(fw);
            
            for(int i = 0; i < jTable2.getRowCount(); i++){//rows
                for(int j = 0; j < jTable2.getColumnCount(); j++){//columns
                    bw.write(jTable2.getValueAt(i, j).toString()+";");
                    
                }
                bw.newLine();
            }
            
            bw.close();
            fw.close();
            
            JOptionPane.showMessageDialog(null,"Update Successfully");
            
            
            
            
            
            
       
            
        } catch (IOException ex) {
            
        }
    }//GEN-LAST:event_btnUpdateTxTActionPerformed

