    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        
        String filePath = "ReservationInfo.txt";
        File Rslist = new File(filePath);
        Scanner scan;
        
        try {
            scan = new Scanner(Rslist); 
            int rinfo=0;
        

            Object RRoom=cbRID.getSelectedItem();
            SimpleDateFormat dFormat = new SimpleDateFormat("yyyy-MM-dd");
            String CheckIn = dFormat.format(dcCheckIn.getDate()) ;
            String CheckOut = dFormat.format(dcCheckOut.getDate()) ;
           
            while (scan.hasNextLine())
            {
            String line = scan.nextLine();
            if (!(line.isEmpty()))
            {
            
            String room=line.split(";")[0];
            String datein=line.split(";")[5];
            String dateout=line.split(";")[6];
            
            
            if(RRoom.equals(room) &&((CheckIn.compareTo(datein)>=0 && CheckIn.compareTo(dateout)<=0) || (CheckOut.compareTo(datein)>=0 && CheckOut.compareTo(dateout)<=0))){
                rinfo=1;
                
            }
            if(rinfo==1 && rinfo!=0){
                JOptionPane.showMessageDialog(null,"This room is already reserved!");
                break;
            
            }
            else if (CheckOut.compareTo(CheckIn)<=0 ){
                JOptionPane.showMessageDialog(null,"CheckOut Date must greater than CheckIn Date");
                break;
            }
            else{
                DefaultTableModel tableModel = (DefaultTableModel) jTable2.getModel();
                tableModel.addRow(new Object[]{cbRID.getSelectedItem(),jTextFieldCustName.getText(),jTextFieldCustID.getText(),jTextFieldCustEmail.getText(),jTextFieldCustCNum.getText(),CheckIn,CheckOut});
            }
            }
            }
            
            
   
        } catch (Exception ex) {
           JOptionPane.showMessageDialog(null,"Please insert all data!");
        } 
        
    
    
    
    
    

        
         
         
    }//GEN-LAST:event_btnAddActionPerformed

    

