    private void btnShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowActionPerformed
        String filePath = "ReservationInfo.txt";
        File Rslist = new File(filePath);

        try{
            BufferedReader br = new BufferedReader(new FileReader(Rslist));
            // get the first line
            // get the columns name from the first line
            // set columns name to the jtable model

            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();

            // get lines from txt file
            Object[] tableLines = br.lines().toArray();

            // extratct data from lines
            // set data to jtable model
            for (Object tableLine : tableLines) {
                String line = tableLine.toString().trim();
                String[] dataRow = line.split(";");
                model.addRow(dataRow);
            }

        }catch (IOException ex){

        }

    }//GEN-LAST:event_btnShowActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
            int selectedRow = jTable1.getSelectedRow();
            DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
            cbRID.setSelectedItem(tableModel.getValueAt(selectedRow, 0).toString());
            jTextFieldCustName.setText(tableModel.getValueAt(selectedRow, 1).toString());
            jTextFieldCustID.setText(tableModel.getValueAt(selectedRow, 2).toString());
            jTextFieldCustEmail.setText(tableModel.getValueAt(selectedRow, 3).toString());
            jTextFieldCustCNum.setText(tableModel.getValueAt(selectedRow, 4).toString());
            Date datein = new SimpleDateFormat("yyyy-MM-dd").parse((String)tableModel.getValueAt(selectedRow, 5));
            dcCheckIn.setDate(datein);
            Date dateout = new SimpleDateFormat("yyyy-MM-dd").parse((String)tableModel.getValueAt(selectedRow, 6));
            dcCheckOut.setDate(dateout);
            
            Date checkin = dcCheckIn.getDate();
            Date checkout = dcCheckOut.getDate();
            long daydiff = Math.abs(checkout.getTime() - checkin.getTime());
            long diff = TimeUnit.DAYS.convert(daydiff,TimeUnit.MILLISECONDS);
            jTextFieldDOS.setText(String.valueOf(diff));
                    
                   
        } catch (ParseException ex) {
            java.util.logging.Logger.getLogger(ReservationInfo.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
            
    }//GEN-LAST:event_jTable1MouseClicked

